// • Operatörleri kullanarak bir matematiksel işlem yazıp konsola yazdırın.

let sayi1 = 30;
let sayi2 = 17;

let toplam = sayi1 + sayi2;
let cikarma = sayi1 - sayi2;
let carpma = sayi1 * sayi2;
let bolme = sayi1 / sayi2;
let kalan = sayi1 % sayi2;

console.log("Toplam:" + toplam);
console.log("Çıkarma:" + cikarma);
console.log("Çarpma:" + carpma);
console.log("Bölme:" + bolme);
console.log("Kalan:" + kalan);
